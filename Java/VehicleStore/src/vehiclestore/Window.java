    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        if (evt.getValueIsAdjusting()) {
            String name = jList1.getSelectedValue().toString();
            
            if (name.matches(".*Engine.*")) {
                jRadioButton2.setSelected(true);
            } else if (name.matches(".*Bodywork.*")) {
                jRadioButton3.setSelected(true);
            } else {
                jRadioButton1.setSelected(true);
            }
            
            Vehicle vehicle = inventory.vehicles.get(jList1.getSelectedIndex());

            jTextField1.setText(Float.toString((Float)(((Engine) vehicle.parts[0]).price)));
            jTextField2.setText(Float.toString((Float)(((Bodywork) vehicle.parts[1]).price)));
            jTextField3.setText(Integer.toString(vehicle.weight));
            jTextField4.setText(((Bodywork) vehicle.parts[1]).description);
            jTextField5.setText(((Bodywork) vehicle.parts[1]).color);
            jTextField6.setText(Integer.toString(((Engine) vehicle.parts[0]).cylinder_quantity));
            jTextField7.setText(Integer.toString(((Engine) vehicle.parts[0]).displacement));
            jTextField8.setText(inventory.brand);
            jTextField9.setText(Integer.toString(((Component) vehicle.parts[0]).number));
        }
    }//GEN-LAST:event_jList1ValueChanged

