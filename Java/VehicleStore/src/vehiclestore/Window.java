    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        String name; 
        
        // Engine price
        Float engine_price= Float.parseFloat(jTextField1.getText());
        // Bodywork price
        Float bodywork_price = Float.parseFloat(jTextField2.getText());
        // Weight
        Integer weight = Integer.parseInt(jTextField3.getText());
        // Description
        String description = jTextField4.getText();
        // Color
        String color = jTextField5.getText();
        // Cylinders
        Integer cylinders = Integer.parseInt(jTextField6.getText());
        // Displacement
        Integer displacement = Integer.parseInt(jTextField7.getText());
        // Brand
        String brand = jTextField8.getText();
        // Number
        Integer number = Integer.parseInt(jTextField9.getText());
        inventory.brand = brand;                    
        
        for (Enumeration<AbstractButton> en = buttonGroup1.getElements(); en.hasMoreElements();) {
            AbstractButton b = en.nextElement();
            if (b.isSelected()) {
                name = b.getActionCommand();
                switch (name) {
                    case "Vehicle":
                        inventory.add(
                           number, engine_price, bodywork_price, weight, 
                           cylinders, displacement, description, color
                          );

                        break;
                    case "Engine":
                        inventory.add(
                           number, engine_price, 0.0f, 0, 
                           cylinders, displacement, "Engine " + description, ""
                          );
                        break;
                    case "Bodywork":
                        inventory.add(
                           number, 0.0f, bodywork_price, 0, 
                           0, 0, "Bodywork " + description, color
                          );
                        break;
                    default:

                    break;
                }
            }
        }
                                  
        LoadModel();
    }//GEN-LAST:event_button1ActionPerformed

    private void LoadModel() {
        DefaultListModel model = new DefaultListModel(); 
        
        for (Vehicle vehicle : inventory.vehicles) {
            model.addElement(((Bodywork)vehicle.parts[1]).description);
        }
        
        jList1.setModel(model);
        jTextArea1.setText(inventory.list());        
    }
    
